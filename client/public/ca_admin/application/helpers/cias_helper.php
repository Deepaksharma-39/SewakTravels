function getDistance($addressFrom, $addressTo, $unit = ''){
    // Google API key
    $apiKey = SW_GOOGLE_API;
	$distance = 0;
    
    // Change address format
    $formattedAddrFrom    = str_replace(' ', '+', $addressFrom);
    $formattedAddrTo     = str_replace(' ', '+', $addressTo);
    
    // Geocoding API request with start address
    $geocodeFrom = file_get_contents('https://maps.googleapis.com/maps/api/geocode/json?address='.$formattedAddrFrom.'&sensor=false&key='.$apiKey);
    $outputFrom = json_decode($geocodeFrom);
    if(!empty($outputFrom->error_message)){
        return array('status' => 0, 'message' => $outputTo->error_message);
    }
    
    // Geocoding API request with end address
    $geocodeTo = file_get_contents('https://maps.googleapis.com/maps/api/geocode/json?address='.$formattedAddrTo.'&sensor=false&key='.$apiKey);
    $outputTo = json_decode($geocodeTo);
    if(!empty($outputTo->error_message)){
		return array('status' => 0, 'message' => $outputTo->error_message);	
    }
	
    // Get latitude and longitude from the geodata
    $latitudeFrom    = $outputFrom->results[0]->geometry->location->lat;
    $longitudeFrom    = $outputFrom->results[0]->geometry->location->lng;
    $latitudeTo        = $outputTo->results[0]->geometry->location->lat;
    $longitudeTo    = $outputTo->results[0]->geometry->location->lng;
    
    // Calculate distance between latitude and longitude
    $theta    = $longitudeFrom - $longitudeTo;
    $dist    = sin(deg2rad($latitudeFrom)) * sin(deg2rad($latitudeTo)) +  cos(deg2rad($latitudeFrom)) * cos(deg2rad($latitudeTo)) * cos(deg2rad($theta));
    $dist    = acos($dist);
    $dist    = rad2deg($dist);
    $miles    = $dist * 60 * 1.1515;

    // Convert unit and return distance
    $unit = strtoupper($unit);
    if($unit == "K"){
        $distance = round($miles * 1.609344, 2).' km';
		return array('status' => 1, 'message' => 'success', 'distance' => $distance);		
    }elseif($unit == "M"){
        $distance = round($miles * 1609.344, 2).' meters';
		return array('status' => 1, 'message' => 'success', 'distance' => $distance);
    }else{
        $distance = round($miles, 2).' miles';
		return array('status' => 1, 'message' => 'success', 'distance' => $distance);
    }
}
   
function getDistanceTime($addressFrom, $addressTo, $unit = ''){	
	// Google API key
    $apiKey = SW_GOOGLE_API;
	$distance = $distance_value = $duration = 0;
	
	// In metric unit. This is default
   $distance_data = file_get_contents('https://maps.googleapis.com/maps/api/distancematrix/json?&origins='.urlencode($addressFrom).'&destinations='.urlencode($addressTo).'&key='.$apiKey);
	  $distance_arr = json_decode($distance_data);
      
	if ($distance_arr->status=='OK') {
		$destination_addresses = $distance_arr->destination_addresses[0];
		$origin_addresses = $distance_arr->origin_addresses[0];
	} else {
		return array('status' => 0, 'message' => $distance_arr->error_message);
	}
	if ($origin_addresses=="" or $destination_addresses=="") {
		return array('status' => 0, 'message' => 'Destination or origin address not found');
	}

	// Get the elements as array
	$elements = $distance_arr->rows[0]->elements;
	
	
	
	$distance = $elements[0]->distance->text;
	$duration = $elements[0]->duration->text;   
	
	$distance_value = round($elements[0]->distance->value / 1000);
	
	return array('status' => 1, 'message' => 'success', 'distance' => $distance, 'distance_value' => $distance_value, 'duration' => $duration);
} 

